/**
 * Type Casting in C
 *
 * Type casting is a way to convert a variable from one data type to another.
 * It can be done in two ways:
 *
 * 1. Implicit Type Casting (Automatic Type Conversion):
 *    - Performed by the compiler automatically.
 *    - Happens when a value of a smaller data type is assigned to a larger data type.
 *    - Example: int to float, char to int.
 *
 * 2. Explicit Type Casting (Manual Type Conversion):
 *    - Performed by the programmer using the cast operator.
 *    - Syntax: (type) expression
 *    - Example: (int) 3.14 converts the float 3.14 to the integer 3.
 *
 * Example:
 * int a = 10;
 * float b = (float) a; // Explicit type casting
 *
 * Type casting is useful when you need to ensure that a value is treated as a specific data type.
 * However, it should be used carefully to avoid data loss or unexpected behavior.
 */